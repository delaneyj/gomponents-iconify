package gala

import (
	g "github.com/maragudk/gomponents"
)

const IconifyVersion = ""

func IconFromName(name string) g.Node {
	switch name {
	case "add": return Add()
	case "airplay": return Airplay()
	case "apple": return Apple()
	case "bag": return Bag()
	case "bell": return Bell()
	case "book": return Book()
	case "brochure": return Brochure()
	case "calendar": return Calendar()
	case "chart": return Chart()
	case "chat": return Chat()
	case "clock": return Clock()
	case "copy": return Copy()
	case "data": return Data()
	case "display": return Display()
	case "editor": return Editor()
	case "file": return File()
	case "fileCodeOne": return FileCodeOne()
	case "fileCodeTwo": return FileCodeTwo()
	case "fileDocument": return FileDocument()
	case "fileError": return FileError()
	case "fileScript": return FileScript()
	case "fileSpreadsheet": return FileSpreadsheet()
	case "fileText": return FileText()
	case "folder": return Folder()
	case "globe": return Globe()
	case "help": return Help()
	case "image": return Image()
	case "issue": return Issue()
	case "layer": return Layer()
	case "lock": return Lock()
	case "mouse": return Mouse()
	case "multi": return Multi()
	case "orbit": return Orbit()
	case "portraitOne": return PortraitOne()
	case "portraitTwo": return PortraitTwo()
	case "radar": return Radar()
	case "remove": return Remove()
	case "search": return Search()
	case "secure": return Secure()
	case "select": return Select()
	case "settings": return Settings()
	case "shield": return Shield()
	case "sidebarLeft": return SidebarLeft()
	case "sidebarRight": return SidebarRight()
	case "store": return Store()
	case "terminal": return Terminal()
	case "tv": return Tv()
	case "unlock": return Unlock()
	case "usb": return Usb()
	case "video": return Video()
	case "window": return Window()
	default:
		panic("Unknown icon name: " + name)
	}
}
